    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nb = datosb.getText();
        String mos = "";
        boolean bandera = false;
        int cont = 1;
         for (int i = 0; i < listaJugador.size(); i++) {
            if(listaJugador.get(i).getNombre().equals(nb)){
            bandera = true;    
            mos = mos + ("Nombre: " + listaJugador.get(i).getNombre() + " \nEdad: " + String.valueOf(listaJugador.get(i).getEdad()) +  " \nVeces que ha jugado: " + String.valueOf(listaJugador.get(i).gettJuegos())+ "\n");
            }
        }
        for (int i = 0; i < historialPartidas.size(); i++) {
            if(historialPartidas.get(i).getNombre().equals(nb)){
                if(historialPartidas.get(i).getGano()!=0){ 
                    mos = mos + ("Partida "+ cont++ +": Ganado\n");
                }
                else{
                    mos = mos +("Partida "+ cont++ +": Perdido | ");
                    mos = mos + ("Barcos hundidos:" + historialPartidas.get(i).getBarcosHundido()  + "\n");
                }
            }
        }
        if(bandera == true){
            JOptionPane.showMessageDialog(null,mos);
        }else{
            JOptionPane.showMessageDialog(null,"Error! Usuario no encontrado");
        }
        
         
        
    }//GEN-LAST:event_jButton1ActionPerformed

